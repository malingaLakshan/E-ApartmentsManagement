                cmd.Parameters.AddWithValue("@description", parking.description);
                cmd.Parameters.AddWithValue("@active_state", parking.active_state);
                cmd.Parameters.AddWithValue("@added_date", parking.added_date);
                cmd.Parameters.AddWithValue("@added_by", parking.added_by);

