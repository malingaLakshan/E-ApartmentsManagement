        {
            SqlConnection conn = new SqlConnection(myconnstrng);
            SqlCommand cmd;
            SqlDataReader dr;

            cmd = new SqlCommand("SELECT name FROM parkings where id = @id", conn);
            cmd.Parameters.AddWithValue("@id", parkingSpaceID);
            conn.Open();

            dr = cmd.ExecuteReader();
            String name = "";

            while (dr.Read())
            {
                name = dr["name"].ToString();


            }
            return name;
        }

